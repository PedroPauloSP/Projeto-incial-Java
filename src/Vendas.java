    private void preencherTabela(){
  ProdutosDAO produtosdao = new ProdutosDAO();
 List<ProdutosDTO> listaProdutos = (List<ProdutosDTO>) produtosdao.Produtosvendidos();
 
 DefaultTableModel tabelaVendas = (DefaultTableModel)tblVendas.getModel();
  tblVendas.setRowSorter(new TableRowSorter(tabelaVendas));
  ProdutosDTO status = new ProdutosDTO();
  //String status = "";
  if(!status.equals("A Venda")){
        
    for (ProdutosDTO P : listaProdutos){
        Object[] obj = new Object[]{
            P.getId(),
            P.getNome(),
            P.getValor(),
            P.getStatus()
                };
        tabelaVendas.addRow(obj);
    }
  } else{
      JOptionPane.showMessageDialog(null, "NÃ£o a vendas");
    }
    }
    
